export const FRAMEWORK_RETENCAO = `
## FRAMEWORK AVANÇADO DE RETENÇÃO

### A Regra dos 3 Segundos
O algoritmo decide se vai distribuir seu vídeo nos primeiros 3 segundos.
• Se o espectador não parou de scrollar → vídeo morreu
• O hook visual + verbal precisa ser INSTANTÂNEO
• Técnicas: movimento brusco, texto grande na tela, frase chocante, zoom rápido

### Micro-Hooks a Cada 3-5 Segundos
Retenção não é só sobre o início — é sobre CADA MOMENTO.
• A cada 3-5 segundos, introduza um novo estímulo:
  - Mudança de ângulo de câmera
  - Novo texto na tela (caption animada)
  - Mudança de tom de voz (sussurro → grito)
  - Corte seco para nova cena
  - Efeito sonoro inesperado
• Se passar mais de 5 segundos sem estímulo novo → risco de queda na retenção

### Pattern Interrupt (Quebra de Padrão)
O cérebro humano se acostuma com padrões — quando o padrão quebra, a atenção volta.
• Tipos de pattern interrupt:
  - **Visual**: mudança abrupta de cenário, zoom in/out rápido, flash de cor
  - **Auditivo**: silêncio repentino, som inesperado, mudança de música
  - **Narrativo**: plot twist, contradição ("Todo mundo acha X… mas é o contrário")
  - **Velocidade**: desacelerar ou acelerar a fala subitamente
• Inserir pelo menos 1 pattern interrupt a cada 8-10 segundos

### Open Loops (Loops Abertos)
Crie perguntas que só serão respondidas DEPOIS.
• "Mas antes de te contar o resultado, precisa entender uma coisa…"
• "O terceiro item da lista é o mais importante — fica até o final"
• "Eu cometi esse erro por 2 anos até descobrir isso…"
• Cada open loop cria uma "dívida cognitiva" — o espectador PRECISA ver até o final para "fechar" o loop

### Visual Pacing (Ritmo Visual)
O ritmo dos cortes define a energia do vídeo:
• **Vídeos rápidos/enérgicos**: cortes a cada 1-2 segundos (trends, humor, lifestyle)
• **Vídeos educativos**: cortes a cada 3-5 segundos (tutoriais, explicações)
• **Storytelling**: cortes a cada 5-8 segundos (histórias, relatos pessoais)
• NUNCA manter o mesmo frame por mais de 10 segundos (exceto em momentos dramáticos intencionais)

### Curva de Retenção Ideal
A curva de retenção de vídeos virais segue este padrão:
• **0-3s**: Pico máximo de atenção (hook) — objetivo: reter 80%+
• **3-10s**: Queda natural (15-20%) — minimizar com micro-hooks
• **10s-50%**: Platô de retenção — manter com open loops e pattern interrupts
• **50%-final**: Subida leve (payoff) — entregar a promessa do hook
• Vídeos virais mantêm 50%+ de retenção ao final. Abaixo de 30% = fracasso.

### Sinais de Baixa Retenção (Red Flags)
• Falar olhando pra baixo ou sem energia nos primeiros 3s
• Introdução longa ("Oi gente, tudo bem? Hoje eu quero falar sobre…")
• Fundo estático e sem movimento por mais de 5s
• Tom de voz monótono sem variação
• Ausência de texto/caption na tela
• Vídeo sem música ou com música genérica sem ritmo
`;

